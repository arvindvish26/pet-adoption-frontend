/**
 * Get a random image for a pet category
 * @param category - The pet category name
 * @param seed - Optional seed for consistent random selection (e.g., pet ID)
 * @returns A random image URL for the category
 */
export function getPetImage(category: string, seed?: string | number): string {
  const categoryImages = PET_IMAGES[category as keyof typeof PET_IMAGES];
  if (!categoryImages) {
    return 'https://images.unsplash.com/photo-1551717743-49959800b1f6?w=400&h=300&fit=crop'; // Default image
  }
  
  // Use seed for consistent random selection
  if (seed) {
    const index = typeof seed === 'string' ? seed.length % categoryImages.length : seed % categoryImages.length;
    return categoryImages[index];
  }
  
  // Random selection
  const randomIndex = Math.floor(Math.random() * categoryImages.length);
  return categoryImages[randomIndex];
}

